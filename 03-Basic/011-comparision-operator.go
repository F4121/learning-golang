package main

import "fmt"

func main() {
	var a = 10
	var b = 11

	// Greater than ">"
	fmt.Println(a > b)
	// Output: false

	// Less than "<"
	fmt.Println(a < b)
	// Output: true

	// More than equal to
	fmt.Println(a >= b)
	// Output: false

	// Less than equal to
	fmt.Println(a <= b)
	// Output: true

	// Equal
	fmt.Println(a == b)
	// Output: false

	// Not equal
	fmt.Println(a != b)
	// Output: true
}
